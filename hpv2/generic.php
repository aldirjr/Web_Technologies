						Warner Bros. bought the film rights to the book in 1999 for a reported £1 million ($1.65 million in 1999). Production began in the United Kingdom in 2000, with Chris Columbus being chosen to create the film from a short list of directors that included Steven Spielberg and Rob Reiner. Rowling insisted that the entire cast be British and Irish, and the film was shot at Leavesden Film Studios and historic buildings around the United Kingdom.
						<br>
						The film was released to cinemas in the United Kingdom and United States on 16 November 2001. It became a critical and commercial success, grossing $975.1 million at the box office worldwide. The highest-grossing film of 2001, it is the forty-first highest-grossing of all-time (2nd at the time of its release; behind Titanic) and the second most successful instalment of the Harry Potter series behind Deathly Hallows – Part 2. The film was nominated for many awards, including Academy Awards for Best Original Score, Best Art Direction and Best Costume Design. It was followed by seven sequels, beginning with Harry Potter and the Chamber of Secrets in 2002 and ending with Harry Potter and the Deathly Hallows – Part 2 in 2011, nearly ten years after the first film's release.</p>
